using System;

namespace MyMath
{
    /// <summary> Operations class </summary>
    public class Operations
    {
        /// <summary> Adding two integers </summary>
        /// <param name="a"> firts integer </param>
        /// <param name="b"> second integer </param>
        /// <returns> sum of two integer </returns>
        public static int Add(int a, int b)
        {
            int result = a + b;
            return result;
        }
    }
}
